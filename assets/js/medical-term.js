// Generate a random medical term
MedicalTermsDB.generateTerm = function() {
  const prefix = this.prefixes[Math.floor(Math.random() * this.prefixes.length)];
  const root = this.roots[Math.floor(Math.random() * this.roots.length)];
  const suffix = this.suffixes[Math.floor(Math.random() * this.suffixes.length)];
  
  // Build term
  let term = '';
  if (Math.random() > 0.3) term += prefix.prefix;
  term += root.root;
  if (Math.random() > 0.2) term += suffix.suffix;
  
  // Build meaning
  let meaning = '';
  if (prefix && Math.random() > 0.3) meaning += prefix.meaning + ' ';
  meaning += root.meaning;
  if (suffix && Math.random() > 0.2) meaning += ' ' + suffix.meaning;
  
  return {
    term: term,
    meaning: meaning.trim(),
    breakdown: {
      prefix: prefix ? prefix.prefix + ' (' + prefix.meaning + ')' : null,
      root: root.root + ' (' + root.meaning + ')',
      suffix: suffix ? suffix.suffix + ' (' + suffix.meaning + ')' : null
    }
  };
};

// Generate question
MedicalTermsDB.generateQuestion = function(difficulty = 'medium') {
  const correctTerm = this.generateTerm();
  const wrongTerms = [];
  
  // Generate wrong answers
  for (let i = 0; i < 3; i++) {
    wrongTerms.push(this.generateTerm());
  }
  
  const questionTypes = [
    {
      type: 'meaning',
      question: `What does "${correctTerm.term}" mean?`,
      correct: correctTerm.meaning,
      wrong: wrongTerms.map(t => t.meaning)
    },
    {
      type: 'term',
      question: `Which term means "${correctTerm.meaning}"?`,
      correct: correctTerm.term,
      wrong: wrongTerms.map(t => t.term)
    },
    {
      type: 'breakdown',
      question: `What is the root word in "${correctTerm.term}"?`,
      correct: correctTerm.breakdown.root,
      wrong: wrongTerms.map(t => t.breakdown.root)
    }
  ];
  
  const selectedType = questionTypes[Math.floor(Math.random() * questionTypes.length)];
  
  // Combine answers and shuffle
  const allAnswers = [selectedType.correct, ...selectedType.wrong];
  for (let i = allAnswers.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [allAnswers[i], allAnswers[j]] = [allAnswers[j], allAnswers[i]];
  }
  
  const correctIndex = allAnswers.indexOf(selectedType.correct);
  
  return {
    question: selectedType.question,
    answers: allAnswers,
    correctIndex: correctIndex,
    explanation: `The term "${correctTerm.term}" breaks down as: ${correctTerm.breakdown.prefix ? correctTerm.breakdown.prefix + ' + ' : ''}${correctTerm.breakdown.root}${correctTerm.breakdown.suffix ? ' + ' + correctTerm.breakdown.suffix : ''}. This gives us the meaning: "${correctTerm.meaning}".`,
    term: correctTerm.term,
    meaning: correctTerm.meaning
  };
};

